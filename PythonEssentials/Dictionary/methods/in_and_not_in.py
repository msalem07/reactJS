# You can use the in and not in to see whether a certain key or value exists in a dictionary.

person = {'name': 'Zophie', 'age': 23}

print('name' in person.keys())

print('Zophie' in person.values())

print('color' in person.keys())

# You can also use in person like this

print('color' in person)

# This is a shorter version of writing 'color' in person.keys()

print('name' in person)

print('Zophie' in person)